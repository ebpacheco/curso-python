# Tipos int e float

# int = Numero inteiro
# O tipo int representa qualquer numero 
# positivo ou negativo.
print (11) # int
print (-11) # int
print (0) # int

# float = Numero com ponto flutuante
# O tipo float representa qualquer numero
# positivo ou negativo com ponto flutuante
print (11.0) # float
print (-11.0) # float
print (0.0) # float

# A funcao type mostra o tipo que o Python inferiu ao valor.
print(type("Eduardo"))
print(type(123))
print(type(-123))
print(type(0))
print(type(-123.0))
print(type(-123.0))
print(type(0.0))