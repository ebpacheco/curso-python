"""
Operadores de comparacao (relacionais)
OP          SIGNIFICADO         EXEMPLO(TRUE)
>           maior               2 > 1
>=          maior ou igual      2 >= 2
<           menor               1 < 2
<=          menor ou igual      2 <= 2
==          igual               'a' == 'a'
!=          diferente           'a' != 'b'
"""

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 > 1
igual = 'a' == 'a'
diferente = 'a' != 'b'

print('Maior =', maior)
print('Maior ou igual = ', maior_ou_igual)
print('Menor =', menor)
print('Menor ou igual = ',menor_ou_igual)
print('Igual =', igual)
print('Diferente = ', diferente)

